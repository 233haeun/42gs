char	*ft_strchr(const char *s, int c)
{
	char	*re;

	re = 0;
	while (*s)
	{
		if (*s == (char)c)
		{
			return ((char *)s);
		}
		s++;
	}
	if (!c)
		return ((char *)s);
	return (re);
}
